import crypto from 'node:crypto'
import fs from 'node:fs'
import path from 'node:path'
import process from 'node:process'

const ROOT = process.cwd()
const allowedEmail = 'tamatm6713@gmail.com'

function argValue(name) {
  const index = process.argv.indexOf(name)
  return index >= 0 ? process.argv[index + 1] : ''
}

const password = argValue('--password') || process.env.OWNER_TOOL_PASSWORD
if (!password) {
  console.error('Thiếu mật khẩu. Chạy: npm run export:vercel-env -- --password "mat-khau-anh-tu-dat"')
  process.exit(1)
}

function readJson(file) {
  return JSON.parse(fs.readFileSync(path.join(ROOT, file), 'utf8'))
}

function readEnvLocal() {
  const file = path.join(ROOT, '.env.local')
  const out = {}
  if (!fs.existsSync(file)) return out
  for (const line of fs.readFileSync(file, 'utf8').split(/\r?\n/)) {
    const trimmed = line.trim()
    if (!trimmed || trimmed.startsWith('#')) continue
    const i = trimmed.indexOf('=')
    if (i <= 0) continue
    out[trimmed.slice(0, i).trim()] = trimmed.slice(i + 1).trim().replace(/^"(.*)"$/, '$1')
  }
  return out
}

function loadRegistry(file) {
  const registry = readJson(file)
  return {
    active_email: registry.active_email || '',
    accounts: (registry.accounts || []).map(account => {
      if (!fs.existsSync(account.token_path)) {
        throw new Error(`Không tìm thấy token: ${account.token_path}`)
      }
      return {
        email: account.email,
        display_name: account.display_name || '',
        token_b64: Buffer.from(fs.readFileSync(account.token_path, 'utf8'), 'utf8').toString('base64'),
      }
    }),
  }
}

const accountA = loadRegistry('account_a_accounts.json')
const accountB = loadRegistry('account_b_accounts.json')
const existingEnv = readEnvLocal()
const storeKey = process.env.OWNER_TOOL_STORE_KEY || existingEnv.OWNER_TOOL_STORE_KEY || crypto.randomBytes(32).toString('base64url')
const payload = {
  active_a: accountA.active_email,
  A: accountA.accounts,
  B: accountB.accounts,
}

const salt = crypto.randomBytes(16).toString('hex')
const key = crypto.scryptSync(password, salt, 32).toString('hex')
const lines = [
  '# Generated by scripts/export-vercel-env.mjs. Do not commit this file.',
  `OWNER_TOOL_ALLOWED_EMAIL=${allowedEmail}`,
  `OWNER_TOOL_PASSWORD_HASH=scrypt:${salt}:${key}`,
  `OWNER_TOOL_AUTH_SECRET=${crypto.randomBytes(32).toString('hex')}`,
  `OWNER_TOOL_STORE_KEY=${storeKey}`,
  `OWNER_TOOL_ACCOUNTS_JSON_B64=${Buffer.from(JSON.stringify(payload), 'utf8').toString('base64')}`,
  '',
]

fs.writeFileSync(path.join(ROOT, '.env.local'), lines.join('\n'), { encoding: 'utf8', mode: 0o600 })
console.log('Đã tạo .env.local cho Vercel/local Node. File này chứa secret và đã được .gitignore.')
